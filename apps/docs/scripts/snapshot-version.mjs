// Freeze the current docs (docs/content/**) into an immutable, committed
// per-release snapshot under docs/versions/<version>/**.
//
// Usage:
//   node scripts/snapshot-version.mjs v0.3          # create the snapshot
//   node scripts/snapshot-version.mjs v0.3 --force  # overwrite if it exists
//
// Workflow: when you cut a release, run this with the new version. The snapshot
// becomes a frozen copy the docs site serves at /<version>/…, while docs/content
// keeps moving as the working/current docs. The newest snapshot is "latest".
//
// docs/content is the single source of truth; docs/versions/* are generated by
// this script but COMMITTED (unlike apps/docs/src/content, which is gitignored).

import { cp, mkdir, readdir, rm, stat } from 'node:fs/promises'
import { existsSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, '../../..')
const SRC_DIR = resolve(REPO_ROOT, 'docs/content')
const VERSIONS_DIR = resolve(REPO_ROOT, 'docs/versions')

// Accept v0.3, v1, v1.2.3 — a leading "v" plus dotted numbers.
const VERSION_RE = /^v\d+(\.\d+)*$/

async function main() {
  const args = process.argv.slice(2)
  const force = args.includes('--force')
  const version = args.find((a) => !a.startsWith('--'))

  if (!version) {
    console.error('Usage: node scripts/snapshot-version.mjs <version> [--force]')
    process.exit(1)
  }
  if (!VERSION_RE.test(version)) {
    console.error(`Invalid version "${version}". Expected e.g. v0.3, v1, v1.2.`)
    process.exit(1)
  }
  if (!existsSync(SRC_DIR)) {
    console.error(`Source docs not found at ${SRC_DIR}`)
    process.exit(1)
  }

  const dest = join(VERSIONS_DIR, version)
  if (existsSync(dest)) {
    if (!force) {
      console.error(
        `${dest} already exists. Re-run with --force to overwrite the snapshot.`
      )
      process.exit(1)
    }
    await rm(dest, { recursive: true, force: true })
  }

  await mkdir(dirname(dest), { recursive: true })
  await cp(SRC_DIR, dest, { recursive: true })

  const files = await countFiles(dest)
  console.log(`[snapshot] froze docs/content -> docs/versions/${version} (${files} files)`)
  console.log('[snapshot] commit docs/versions/' + version + ' to publish the version.')
}

async function countFiles(dir) {
  let n = 0
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name)
    if (entry.isDirectory()) n += await countFiles(full)
    else if (/\.mdx?$/.test(entry.name)) n++
  }
  return n
}

await main()
