#!/usr/bin/env bun

/**
 * Generate skill documentation from codebase
 *
 * This script dynamically scans the codebase to generate comprehensive
 * API documentation for the Agent Skill.
 *
 * Usage: bun run scripts/generate-skill-docs.ts
 */

import { readdirSync, readFileSync, statSync, writeFileSync } from 'fs'
import { join } from 'path'

const ROOT = process.cwd()
const SKILL_DIR = join(ROOT, 'skills')

// Types extracted from the codebase
interface ChartModule {
  charts: Record<string, { description?: string }>
}

interface QueryConfig {
  name: string
  description?: string
}

/**
 * Recursively find all files matching a pattern
 */
function findFiles(dir: string, pattern: RegExp): string[] {
  const files: string[] = []

  function traverse(currentPath: string) {
    const entries = readdirSync(currentPath)

    for (const entry of entries) {
      const fullPath = join(currentPath, entry)
      const stat = statSync(fullPath)

      if (stat.isDirectory()) {
        // Skip node_modules and hidden directories
        if (!entry.startsWith('.') && entry !== 'node_modules') {
          traverse(fullPath)
        }
      } else if (stat.isFile() && pattern.test(entry)) {
        files.push(fullPath)
      }
    }
  }

  traverse(dir)
  return files
}

/**
 * Extract chart names from domain chart modules
 */
function extractChartNames(): string[] {
  const chartFiles = findFiles(join(ROOT, 'lib/api/charts'), /-charts\.ts$/)

  const allCharts: Set<string> = new Set()

  for (const filePath of chartFiles) {
    try {
      const content = readFileSync(filePath, 'utf-8')

      // Extract chart names from export const xxxCharts = { ... }
      // Pattern: 'chart-name': ({ ... }) => ({
      const chartRegex = /'([^']+)':\s*\(/g
      let match
      while ((match = chartRegex.exec(content)) !== null) {
        allCharts.add(match[1])
      }
    } catch (e) {
      console.warn(`Could not read ${filePath}:`, e.message)
    }
  }

  return Array.from(allCharts).sort()
}

/**
 * Extract query config names from query-config files
 */
function extractQueryConfigNames(): string[] {
  const queryFiles = findFiles(join(ROOT, 'lib/query-config'), /\.ts$/)

  const configs: Set<string> = new Set()

  for (const filePath of queryFiles) {
    try {
      const content = readFileSync(filePath, 'utf-8')

      // Extract config name: export const xxxConfig: QueryConfig = {
      const configRegex = /export\s+const\s+(\w+)Config:\s*QueryConfig/g
      let match
      while ((match = configRegex.exec(content)) !== null) {
        const name = match[1]
          .replace(/([A-Z])/g, '-$1')
          .toLowerCase()
          .replace(/^-/, '')
        configs.add(name)
      }
    } catch (e) {
      console.warn(`Could not read ${filePath}:`, e.message)
    }
  }

  return Array.from(configs).sort()
}

