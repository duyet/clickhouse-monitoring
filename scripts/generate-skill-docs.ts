/**
 * Generate dashboard pages reference
 */
function generateDashboardPages(): string {
  // Read menu.ts to extract page structure
  const menuPath = join(ROOT, 'menu.ts')
  const menuContent = readFileSync(menuPath, 'utf-8')

  // Parse menu items using a more robust approach
  // Look for objects with title, href, description properties
  const itemRegex =
    /{\s*title:\s*['"]([^'"]+)['"][\s\S]*?href:\s*['"]([^'"]+)['"][\s\S]*?description:\s*['"]([^'"]+)['"]/g

  const pages: Array<{ title: string; href: string; description: string }> = []

  let match
  while ((match = itemRegex.exec(menuContent)) !== null) {
    pages.push({
      title: match[1],
      href: match[2],
      description: match[3],
    })
  }

  // Also look for simpler items without description
  const simpleRegex =
    /{\s*title:\s*['"]([^'"]+)['"][\s\S]*?href:\s*['"]([^'"]+)['"]/g
  while ((match = simpleRegex.exec(menuContent)) !== null) {
    // Skip if already added
    if (!pages.some((p) => p.title === match[1] && p.href === match[2])) {
      pages.push({
        title: match[1],
        href: match[2],
        description: '',
      })
    }
  }

  // Filter out parent items (empty href or query-only href)
  const leafPages = pages.filter(
    (p) => p.href && p.href !== '' && !p.href.includes('?name=')
  )

  return `# Dashboard Pages

Complete list of all dashboard pages (${leafPages.length} total).

| Page | Path | Description |
|------|------|-------------|
${leafPages
  .map(
    (page) =>
      `| **${page.title}** | \`${page.href || '/'}\` | ${page.description} |`
  )
  .join('\n')}

---
*Generated by \`scripts/generate-skill-docs.ts\` from ${new Date().toISOString()}*
`
}

/**
 * Main execution
 */
function main() {
  console.log('🔍 Scanning codebase for API endpoints...')

  const chartNames = extractChartNames()
  console.log(`   Found ${chartNames.length} chart endpoints`)

  const queryNames = extractQueryConfigNames()
  console.log(`   Found ${queryNames.length} table query endpoints`)

  // Generate API reference
  const apiReference = generateAPIReference(chartNames, queryNames)
  writeFileSync(join(SKILL_DIR, 'references/api-list.md'), apiReference)
  console.log('✅ Generated references/api-list.md')

  // Generate dashboard pages reference
  const dashboardPages = generateDashboardPages()
  writeFileSync(
    join(SKILL_DIR, 'references/dashboard-pages.md'),
    dashboardPages
  )
  console.log('✅ Generated references/dashboard-pages.md')

  console.log('\n📚 Skill documentation updated successfully!')
  console.log('\nNext steps:')
  console.log('  1. Review the generated files')
  console.log('  2. Update SKILL.md to reference the new sections')
  console.log('  3. Commit the changes')
}

main()
